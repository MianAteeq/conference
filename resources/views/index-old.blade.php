function calculateTotal() {
    var checkIDs = [];

    // Get all checked workshop checkbox values
    $("input[type=checkbox]:checked").each(function () {
        checkIDs.push(this.value);
    });

    let total = 0;

    // Parse workshops from Blade
    let types = @json($types_shops);

    checkIDs.forEach((item) => {
        let exists = types.filter((record) => record.id == item);
        if (exists.length > 0) {
            let price = parseFloat(exists[0].price);
            if (!isNaN(price)) {
                total += price;
            }
        }
    });

    // Category dropdown (Conference)
    let regType = $("input[name='registration_type']:checked").val();
    console.log(regType,'regType');
    if (regType === "conference"){
        
    
    let category = $("select[name='category']").val();
    let types_categories = @json($types);

    let cats = types_categories.filter((item) => item.id == category);
    if (cats.length > 0) {
        let price = parseFloat(cats[0].price);
        if (!isNaN(price)) {
            total += price;
        }
    }
    }

    // Accompanying persons (only for conference)
    
    let accompanyingCount = parseInt($("#accompanying_count").val()) || 0;

    if (regType === "conference" && accompanyingCount > 0) {
        let extra = accompanyingCount * 5000;
        total += extra;

        // Add row for accompanying persons
        let accRow = `
            <tr id="accompanying-row">
                <td class="wpforms-order-summary-item-label">Accompanying Persons</td>
                <td class="wpforms-order-summary-item-quantity">${accompanyingCount}</td>
                <td class="wpforms-order-summary-item-price" style="width: 12ch;">
                    Rs ${extra.toFixed(2)}
                </td>
            </tr>
        `;
        $('#accompanying-row').remove();
        $('#item-body').append(accRow);
    } else {
        $('#accompanying-row').remove();
    }

    // Build total row
    let html = `
        <tr class="wpforms-order-summary-preview-total" id="total-tr">
            <td class="wpforms-order-summary-item-label" valign="top"><strong>Total</strong></td>
            <td class="wpforms-order-summary-item-quantity" valign="top"></td>
            <td class="wpforms-order-summary-item-price" valign="top" style="width: 12ch;font-size: 15px;">
                <strong>Rs ${total.toFixed(2)}</strong>
            </td>
        </tr>
    `;

    // Replace previous total row
    $('#total-tr').remove();
    $('#item-body').append(html);

    // Update hidden total input (important for backend)
    $("#wpforms-296-field_24").val(total);
}

